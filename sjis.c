/********************************************************************************/
/*		sjis_parse																*/
/*		文字列から１文字（1/2 バイトコード）取り出し							*/
/*		return: キャラゼネコード												*/
/********************************************************************************/
int sjis_parse(const char *str, int *byte)
{
	int c, ku, ten;
	
	c = *str & 0xff;
	if(c>=0x81 && c<=0x9f){
		ku = (c - 0x81)*2 + 1;				// 1〜62
	}
	else if(c>=0xe0 && c<=0xef){			// 63〜94
		ku = (c - 0xe0)*2 + 63;
	}
	else{
		*byte = 1;							// 1 バイトコード
		return c;
	}
	
	c = str[1] & 0xff;
	if(c>=0x40 && c<=0x7e){					// 1〜63
		ten = c - 0x40 + 1;
	}
	else if(c>=0x80 && c<=0x9e){			// 64〜94
		ten = c - 0x80 + 64;
	}
	else if(c>=0x9f && c<=0xfc){			// 1〜94
		ku++;
		ten = c - 0x9f + 1;
	}
	else{									// 1 バイトコード  エラー
		*byte = 1;
		return c;
	}
	
    *byte = 2;
	return (ku-1)*94 + ten /* - 1 */;
}
/********************************************************************************/
/*		ut_printf																*/
/********************************************************************************/
void ut_printf(const char *fmt, ...)
{
	char buf[200];
	va_list ap;
	
	va_start(ap, fmt);
	vsprintf(buf, fmt, ap);
	va_end(ap);
	ut_puts(buf);
}
/********************************************************************************/
/*		ut_puts																	*/
/*		半角/全角の文字列出力													*/
/********************************************************************************/
void ut_puts(const char *str)
{
	int byte;
	
	for(;;){
		byte = ut_putc(str);
		if(byte==0)
			return;
		str += byte;
	}
}
/********************************************************************************/
/*		ut_putc																	*/
/*		半角/全角の putc 1文字、return: バイト数(0 or 1 or 2)					*/
/********************************************************************************/
void ut_scroll()
{
	int x, y, c;
	
	for(y=10; y<Y_RES; y++){
		for(x=0; x<X_RES; x++){
			c = pget(x, y);
			pset(x, y-10, c);
		}
	}
	for(y=Y_RES-10; y<Y_RES; y++){
		for(x=0; x<X_RES; x++){
			pset(x, y, BLACK);
		}
	}
}

void ut_nl()
{
	lcd_x = 0;
	if(++lcd_y >= MAX_LINE){
		ut_scroll();						// スクロール
		lcd_x = 0;
		lcd_y = MAX_LINE - 1;
	}
}

//	半角/全角の putc 1文字、return: バイト数(0 or 1 or 2)
int ut_putc(const char *str)
{
	int c, byte;
	
	c = sjis_parse(str, &byte);
	if(c==0)
		return 0;							// 0 byte
	if(byte==1){
		if(c=='\n'){
			ut_nl();
			return 1;
		}
		if(lcd_x >= MAX_CHAR){
			ut_nl();
		}
		char_disp(lcd_x*6, lcd_y*10, c);
		++lcd_x;
		return 1;
	}
	else{
		if(lcd_x >= MAX_CHAR-1){
			ut_nl();
		}
		sjis_disp(lcd_x*6, lcd_y*10, c);
		lcd_x += 2;
		return 2;
	}
}
/********************************************************************************/
/*		str_cdisp																*/
/*		半角/全角																*/
/********************************************************************************/
void str_cdisp(int y, const char *str)
{
	int x, len;
	
    len = strlen(str)*6;
    if(160 >= len){
        x = (160-len)/2;
    }
    else{
        x = 0-(len-160)/2;
    }
	str_disp(x, y, str);
}
/********************************************************************************/
/*		str_disp																*/
/*		半角/全角																*/
/********************************************************************************/
void str_disp(int x, int y, const char *str)
{
	int c, byte;
	
	for(;;){
		c = sjis_parse(str, &byte);
		if(c==0){
			return;
		}
		if(byte==1){
			char_disp(x, y, c);
			x += 6;
            str++;
		}
		else{
			sjis_disp(x, y, c);
			x += 12;
            str+=2;
		}
	}
}
/********************************************************************************/
/*		char_disp																*/
/*		半角文字のみ															*/
/*		5x7 のフォント, 枠を付けて 7x9 のサイズにする							*/
/********************************************************************************/
void char_disp(int x, int y, int c)
{
	int x1, y1, c1;
	
	for(x1=0; x1<7; x1++){					// 上の１行はスペース
		if(rev_flag==0){
			pset(x+x1, y, back_color);
		}
		else{
			pset(x+x1, y, text_color);
		}
	}
	for(y1=0; y1<9; y1++){
		if(rev_flag==0){
			pset(x, y+y1, back_color);
			pset(x+7-1, y+y1, back_color);
		}
		else{
			pset(x, y+y1, text_color);
			pset(x+7-1, y+y1, text_color);
		}
	}
	for(x1=0; x1<5; x1++){
		c1 = chara_gene[c & 0xff][x1];
		for(y1=0; y1<8; y1++){
			if(rev_flag==0){
				if(c1 & (1<<y1)){
					pset(x+x1+1, y+y1+1, text_color);
				}
				else{
					pset(x+x1+1, y+y1+1, back_color);
				}
			}
			else{
				if(c1 & (1<<y1)){
					pset(x+x1+1, y+y1+1, back_color);
				}
				else{
					pset(x+x1+1, y+y1+1, text_color);
				}
			}
		}
	}
}
/********************************************************************************/
/*		sjis_disp																*/
/*		全角文字のみ															*/
/*		12x12 のフォント														*/
/********************************************************************************/
void sjis_disp(int x, int y, int c)
{
	int i, j, bit, x1, y1, c1;
	
	for(i=0; i<12*13; i++){
		x1 = i%12;
		y1 = i/12;
		j = (i /*-12*/)/8;
		bit = 0x80 >> ((i /*-12*/)%8);
		c1 = KanjiFont12b[c][j];
		if((c1 & bit) && (/*i>=12 &&*/ i<12*12)){
			if(rev_flag==0){
				pset(x+x1, y+y1, text_color);
			}
			else{
				pset(x+x1, y+y1, back_color);
			}
		}
		else{
			if(rev_flag==0){
				pset(x+x1, y+y1, back_color);
			}
			else{
				pset(x+x1, y+y1, text_color);
			}
		}
	}
}
void sjis_disp_rev(int x, int y, int c)
{
	int i, ii, j, bit, x1, y1, c1;
	
	for(i=0; i<12*13; i++){
		ii = 12*13-1-i;
		x1 = i%12;
		y1 = i/12;
		j = (ii /*-12*/)/8;
		bit = 0x80 >> ((ii /*-12*/)%8);
		c1 = KanjiFont12b[c][j];
		if((c1 & bit) && (/*ii>=12 &&*/ ii<12*12)){
			if(rev_flag==0){
				pset(x+x1, y+y1, text_color);
			}
			else{
				pset(x+x1, y+y1, back_color);
			}
		}
		else{
			if(rev_flag==0){
				pset(x+x1, y+y1, back_color);
			}
			else{
				pset(x+x1, y+y1, text_color);
			}
		}
	}
}

/********************************************************************************/
/*		lcd_clear																*/
/********************************************************************************/
void lcd_clear(int color)
{
	int x, y;
	
	for(y=0; y<Y_RES; y++){
		for(x=0; x<X_RES; x++){
			pset(x, y, color);
		}
	}
}

void lcd_clearA(int x1, int y1, int x2, int y2, int color)
{
	int x, y;
	
	for(y=y1; y<=y2; y++){
		for(x=x1; x<=x2; x++){
			pset(x, y, color);
		}
	}
}
/********************************************************************************/
/*		write_square															*/
/********************************************************************************/
void write_square(int x, int y, int w, int h, const char *c)
{
	int xx, yy;
	
	for(yy=y; yy<y+h; yy++){
		for(xx=x; xx<x+w; xx++){
			pset(xx, yy, *c++);
		}
	}
}
/********************************************************************************/
/*		read_square																*/
/********************************************************************************/
void read_square(int x, int y, int w, int h, char *c)
{
	int xx, yy;
	
	for(yy=y; yy<y+h; yy++){
		for(xx=x; xx<x+w; xx++){
			*c++ = pget(xx, yy);
		}
	}
}


