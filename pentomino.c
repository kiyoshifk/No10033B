static struct menu pen_menu[]={
	{0, 70, 1, "　完成図表\示　"},
	{0, 90, 2, "　経過図表\示　"},
	{0,0,0,0}
};

void pentomino_main()
{
	int cmd;
	
	lcd_clear(GRAY);
	str_cdisp(70, "　ペントミノ　");
	str_cdisp(90, "K.Fukumoto");
	sw_updown();
	
    lcd_clear(GRAY);
	for(;;){
		cmd = menu_select(pen_menu);
		if(cmd==1){                         // 完成図
			pentomino(1);
			return;
		}
		else if(cmd==2){                    // 経過図
            sw_up();
			pentomino(0);
			return;
		}
	}
}






