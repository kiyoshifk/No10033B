#include "app.h"


#define macrox(pos) ((pos)%12)			// 10x6 の盤、縁を付けて 12x8
#define macroy(pos) ((pos)/12)
#define macropos(x,y) ((x)+(y)*12)

struct pen_move{
	int pos;							// 置く pos
//	int peni[i][k][cnt];				// i=0〜11, k=0〜7, cnt=0〜4
	int i;
	int k;
};

static const char pen_color[12]={
	BRUE,RED,MAGENTA,GREEN,CYAN,YELLOW,GRAY,BRUE_U,BRUE_D,MAGENTA_U,MAGENTA_D,GREEN_U,
};
static struct pen_move pen_move[12];
static int peni[12][8][5];						// pos 定義, stopper = 10000
static char pen_ban[8][12];					// 盤


extern const char pen[12][8][8][5];

/********************************************************************************/
/*		pen_disp_1																*/
/*		20x20 の正方形を表示する												*/
/*		x=1〜10, y=1〜6															*/
/********************************************************************************/
static void pen_disp_1(int pos, int color)
{
	int x, y;
	
	x = macrox(pos);
	y = macroy(pos);
	if(x<1 || x>10 || y<1 || y>6){
		printf("pen_disp_1 err1 x=%d y=%d\n", x, y);
		for(;;)
			;
	}
	lcd_clearA((x-1)*14+10, (y-1)*20+20, (x-1)*14+10+14-1, (y-1)*20+20+20-1, color);
}

static void pen_waku()
{
	lcd_clearA(  10-1,   20-1, 140+10,   20-1, BLACK);
	lcd_clearA(  10-1, 120+20, 140+10, 120+20, BLACK);
	lcd_clearA(  10-1,   20-1,   10-1, 120+20, BLACK);
	lcd_clearA(140+10,   20-1, 140+10, 120+20, BLACK);

	lcd_clearA(10-1, 20-1, 140+10, 120+20, BLACK);
}
/********************************************************************************/
/*		pen_test_move															*/
/*		return 1:置ける、0:置けない											*/
/********************************************************************************/
static int pen_test_move(struct pen_move *move)
{
	int cnt, p;
    char *ban = (char*)pen_ban;
	
	for(cnt=0; cnt<5; cnt++){
		p = peni[move->i][move->k][cnt];
		if(ban[move->pos + p])		// 置けない
			return 0;
	}
	return 1;							// 置ける
}
/********************************************************************************/
/*		pen_put_to_disp															*/
/********************************************************************************/
static void pen_put_to_disp(struct pen_move *move)
{
	int i, cnt, p, pp, x, y, U, D, R, L;
	
	/*** 四角表示	***/
	for(cnt=0; cnt<5; cnt++){
		pen_disp_1(move->pos + peni[move->i][move->k][cnt], pen_color[move->i]);
	}
	
	/*** 枠表示	***/
	for(cnt=0; cnt<5; cnt++){
		p = peni[move->i][move->k][cnt];
		U = D = R = L = 1;
		for(i=0; i<5; i++){
			pp = peni[move->i][move->k][i];
			if(p-12 == pp)				// 上不要
				U = 0;
			if(p+12 == pp)				// 下不要
				D = 0;
			if(p-1 == pp)				// 左不要
				L = 0;
			if(p+1 == pp)				// 右不要
				R = 0;
		}
		x = macrox(move->pos + p);
		y = macroy(move->pos + p);
		if(U)
			lcd_clearA((x-1)*14+10,      (y-1)*20+20, (x-1)*14+10+14-1,      (y-1)*20+20, BLACK);
		if(D)
			lcd_clearA((x-1)*14+10, (y-1)*20+20+20-1, (x-1)*14+10+14-1, (y-1)*20+20+20-1, BLACK);
		if(L)
			lcd_clearA((x-1)*14+10,      (y-1)*20+20,      (x-1)*14+10, (y-1)*20+20+20-1, BLACK);
		if(R)
			lcd_clearA((x-1)*14+10+14-1, (y-1)*20+20, (x-1)*14+10+14-1, (y-1)*20+20+20-1, BLACK);
	}
}
/********************************************************************************/
/*		pen_remove_from_disp													*/
/********************************************************************************/
static void pen_remove_from_disp(struct pen_move *move)
{
	int cnt;
	
	/*** 四角表示	***/
	for(cnt=0; cnt<5; cnt++){
		pen_disp_1(move->pos + peni[move->i][move->k][cnt], BLACK);
	}
}
/********************************************************************************/
/*		make_peni																*/
/********************************************************************************/
static void make_peni()
{
	int i, k, x, y, cnt, top_pos;
	
	for(i=0; i<12; i++){				// 12個の駒について
		for(k=0; k<8; k++){				// 8回転について
			if(pen[i][k][0][0] < 0){
				peni[i][k][0] = 10000;	// stopper
				break;
			}
			top_pos = -1;
			cnt = 0;
			for(x=0; x<5; x++){
				for(y=0; y<8; y++){
					if(pen[i][k][y][x]){
						if(top_pos < 0){
							if(x != 0 || y != 3){	// top pos check
								printf("make_peni err1, i=%d k=%d y=%d x=%d\n", i, k, y, x);
								for(;;)
									;
							}
							top_pos = macropos(x,y);
						}
						peni[i][k][cnt] = macropos(x,y) - top_pos;
						++cnt;
					}
				}
			}
			if(cnt != 5){				// 四角の数が５個であること
				printf("make_peni err2, i=%d k=%d cnt=%d\n", i, k, cnt);
				for(;;)
					;
			}
		}
	}
}
/********************************************************************************/
/*		pen_ban_init															*/
/********************************************************************************/
static void pen_ban_init()
{
	int x, y;
	
	memset(pen_ban, 0, sizeof(pen_ban));
	for(y=0; y<8; y++){
		pen_ban[y][0] = 1;
		pen_ban[y][12-1] = 1;
	}
	for(x=0; x<12; x++){
		pen_ban[0][x] = 1;
		pen_ban[8-1][x] = 1;
	}
}
/********************************************************************************/
/*		pen_put_to_ban															*/
/********************************************************************************/
static void pen_put_to_ban(struct pen_move *move)
{
	int cnt, p;
	char *ban = (char*)pen_ban;
	
	if(peni[move->i][move->k][0]==10000){
		printf("put_to_ban err1 i=%d k=%d\n", move->i, move->k);
		for(;;)
			;
	}
	for(cnt=0; cnt<5; cnt++){
		p = peni[move->i][move->k][cnt];
		ban[move->pos + p] = 1;
	}
}
/********************************************************************************/
/*		pen_remove_from_ban														*/
/********************************************************************************/
static void pen_remove_from_ban(struct pen_move *move)
{
	int cnt, p;
	char *ban = (char*)pen_ban;
	
	if(peni[move->i][move->k][0]==10000){
		printf("remove_from_ban err1 i=%d k=%d\n", move->i, move->k);
		for(;;)
			;
	}
	for(cnt=0; cnt<5; cnt++){
		p = peni[move->i][move->k][cnt];
		ban[move->pos + p] = 0;
	}
}
/********************************************************************************/
/*		pen_top_pos																*/
/*		盤の空き位置のうち、左の上を返す										*/
/********************************************************************************/
static int pen_top_pos()
{
	int x, y, p;
	char *ban = (char*)pen_ban;
    
	for(x=1; x<=10; x++){
		for(y=1; y<=6; y++){
			p = macropos(x,y);
			if(ban[p]==0)
				return p;
		}
	}
	printf("pen_top_pos err1\n");
	for(;;)
		;
    return 0;
}
