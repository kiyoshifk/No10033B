/********************************************************************************/
/*		pen_disp_all															*/
/********************************************************************************/
static void pen_disp_all()
{
	int i;
	
	drawcount = 0;
	while(drawcount==0)
		;
	for(i=0; i<12; i++){
        if(i==6){
        	drawcount = 0;
        	while(drawcount==0)
        		;
        }
		pen_put_to_disp(&pen_move[i]);
	}
}
/********************************************************************************/
/*		pentomino																*/
/********************************************************************************/
static void pentomino(int fast)
{
	int sw, ii, m, up, use[12], count, time;
	
    make_peni();
    pen_ban_init();
    lcd_clear(WHITE);
    pen_waku();
	memset(use, 0, sizeof(use));
    count = 0;
	ii = 0;
	up = 1;
	time = TickCount;
	for(;;){
		if(TickCount-time > 6){
			drawcount = 0;
			while(drawcount==0)
				;
			time = TickCount;
		}
		if(fast==0){
			sw = sw_sense();
			if(sw){
//				sw_downup();
				return;
			}
		}
//		pen_test();//AAAAA 盤の縁をテスト
		if(up==1){							// 前進した
			for(m=0; m<12; m++){
				if(use[m]==0){
					use[m] = 1;
					break;
				}
			}
			if(m==12){
				printf("pentomino err1 ii=%d\n", ii);
				for(;;)
					;
			}
			pen_move[ii].i = m;
			pen_move[ii].k = 0;
			pen_move[ii].pos = pen_top_pos();
		}
		else{								// 続きを実行 (後退した)
            if(up==0){
    			pen_remove_from_ban(&pen_move[ii]);
    			if(fast==0)
					pen_remove_from_disp(&pen_move[ii]);
            }
			++pen_move[ii].k;
			if(pen_move[ii].k >= 8 || peni[pen_move[ii].i][pen_move[ii].k][0] == 10000){	// 回転方向全部試した
				for(m=pen_move[ii].i+1; m<12; m++){
					if(use[m]==0){
						use[m] = 1;
						break;
					}
				}
				use[pen_move[ii].i] = 0;
				if(m==12){						// 全部テストしたから後退する
					up = 0;
					ii--;
					if(ii<0){					// 終了
						ut_printf("pentomino end\n");
                        sw_updown();
						return;
					}
					continue;
				}
				pen_move[ii].i = m;
				pen_move[ii].k = 0;
			}
		}
		if(pen_test_move(&pen_move[ii])){	// 置けるならば
			pen_put_to_ban(&pen_move[ii]);	// 盤に置く
			if(fast==0){
				pen_put_to_disp(&pen_move[ii]);	// 表示する
				wait_ms(100);
			}
			if(ii== 12-1){					// 完成した
				if(fast==0){
					lcd_clear(RED);
					pen_waku();
					pen_disp_all();
					wait_ms(10000);
					
					lcd_clear(WHITE);
					pen_waku();
					pen_disp_all();
				}
				else{
                    lcd_x = lcd_y = 0;
                    ut_printf("count=%d\n", ++count);
					pen_disp_all();
					sw = sw_sense();
					if(sw){
//						sw_downup();
						return;
					}
				}
				up = 0;
			}
			else{							// 未完成, 前進する
				up = 1;
				ii++;
			}
		}
		else{								// 置けない
			up = 2;							// 続きを実行
		}
	}
}
